export type SiteThemeName = (typeof SITE_THEME_OPTIONS)[number]["value"];
export type ProductCardVariant =
  (typeof PRODUCT_CARD_VARIANT_OPTIONS)[number]["value"];
export type ProductDetailsVariant =
  (typeof PRODUCT_DETAILS_VARIANT_OPTIONS)[number]["value"];

export type SiteAppearanceSettings = {
  siteTheme: SiteThemeName;
  productCardVariant: ProductCardVariant;
  productDetailsVariant: ProductDetailsVariant;
};

export const DEFAULT_SITE_THEME: SiteThemeName = "light";
export const DEFAULT_PRODUCT_CARD_VARIANT: ProductCardVariant = "classic";
export const DEFAULT_PRODUCT_DETAILS_VARIANT: ProductDetailsVariant = "classic";

const LEGACY_THEME_ALIASES: Record<string, SiteThemeName> = {
  dark: "slate",
  sepia: "amber",
  blue: "cobalt",
  orange: "coral",
  green: "emerald",
};

export function normalizeSiteTheme(value?: string | null): SiteThemeName {
  if (SITE_THEME_OPTIONS.some((item) => item.value === value)) {
    return value as SiteThemeName;
  }

  if (value && LEGACY_THEME_ALIASES[value]) {
    return LEGACY_THEME_ALIASES[value];
  }

  return DEFAULT_SITE_THEME;
}

export function normalizeProductCardVariant(
  value?: string | null,
): ProductCardVariant {
  return PRODUCT_CARD_VARIANT_OPTIONS.some((item) => item.value === value)
    ? (value as ProductCardVariant)
    : DEFAULT_PRODUCT_CARD_VARIANT;
}

export function normalizeProductDetailsVariant(
  value?: string | null,
): ProductDetailsVariant {
  return PRODUCT_DETAILS_VARIANT_OPTIONS.some((item) => item.value === value)
    ? (value as ProductDetailsVariant)
    : DEFAULT_PRODUCT_DETAILS_VARIANT;
}

export function buildSiteAppearanceSettings(input?: {
  siteTheme?: string | null;
  productCardVariant?: string | null;
  productDetailsVariant?: string | null;
}): SiteAppearanceSettings {
  return {
    siteTheme: normalizeSiteTheme(input?.siteTheme),
    productCardVariant: normalizeProductCardVariant(input?.productCardVariant),
    productDetailsVariant: normalizeProductDetailsVariant(
      input?.productDetailsVariant,
    ),
  };
}
